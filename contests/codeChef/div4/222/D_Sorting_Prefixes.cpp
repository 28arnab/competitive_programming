void solve()
{
    int n;
    cin >> n;
    vector<int> a(n);
    for (int i = 0; i < n; i++)
        cin >> a[i];

    int r = -1;
    for (int i = 0; i < n; ++i)
        if (a[i] != i + 1)
            r = i;

    if (r == -1)
    {
        cout << 0 << '\n';
    }
    else
    {
        int m = INT_MAX;
        for (int i = r; i < n; ++i)
            m = min(m, a[i]);
        cout << m << '\n';
    }
}

int32_t main()
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int t = 1;
    cin >> t;
    while (t--)
    {
        solve();
    }
    return 0;
}